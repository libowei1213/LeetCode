from collections import Counter


def gcd(m, n):
    m, n = (m, n) if m >= n else (n, m)
    while n:
        m, n = n, m % n
    return m


class Solution:
    def hasGroupsSizeX(self, deck):
        """
        :type deck: List[int]
        :rtype: bool
        """

        if not deck:
            return False

        counter = Counter()
        counter.update(deck)

        min_count = min(counter.values())
        if min_count < 2:
            return False

        values = list(counter.values())

        temp = values[0]
        for i in range(1, len(values)):
            temp = gcd(temp, values[i])
        if temp < 2:
            return False

        for value in counter.values():
            if value % temp != 0:
                return False
        return True


